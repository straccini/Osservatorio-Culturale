/**
 * Struttura JSON IdentityMap:
 * {
 *   dims: { D1: true, D2: false, D3: false, D4: true, ... D10: false },
 *   ambito: "nazionale" | "locale",
 *   regione: "" | "Puglia" | "Toscana" | ...
 *   notifyOnlyMatches: false,
 *   autoGenerated: false,
 *   lastUpdate: "2026-05-26T10:30:00Z"
 * }
 */

var IDENT_COL_NAME = 'IdentityMap';
var IDENT_DEFAULT = {
  dims: { D1: false, D2: false, D3: false, D4: false, D5: false, D6: false, D7: false, D8: false, D9: false, D10: false },
  ambito: 'nazionale',
  regione: '',
  notifyOnlyMatches: false,
  autoGenerated: false,
  lastUpdate: ''
};

// Mapping dimensioni → ambiti OC (da Constants.js matrixDims)
var IDENT_DIM_TO_AMBITI = {
  D1: [1],       // Brand & Identity → Amb.1
  D2: [3],       // Collection & Heritage → Amb.3
  D3: [3],       // Facility → Amb.3
  D4: [3],       // Program → Amb.3
  D5: [3],       // Service → Amb.3
  D6: [5],       // Digital → Amb.5
  D7: [2],       // Accessibilita → Amb.2
  D8: [1, 4],    // Audience → Amb.1 + Amb.4
  D9: [5],       // Governance → Amb.5
  D10: [4]       // Welfare → Amb.4
};

var IDENT_REGIONI = [
  'Abruzzo', 'Basilicata', 'Calabria', 'Campania', 'Emilia-Romagna',
  'Friuli-Venezia Giulia', 'Lazio', 'Liguria', 'Lombardia', 'Marche',
  'Molise', 'Piemonte', 'Puglia', 'Sardegna', 'Sicilia',
  'Toscana', 'Trentino-Alto Adige', 'Umbria', "Valle d'Aosta", 'Veneto'
];

// Nomi dimensioni per la UI
var IDENT_DIM_NAMES = {
  D1:  'Brand & Identity',
  D2:  'Collection & Heritage',
  D3:  'Facility & Spatial Experience',
  D4:  'Program & Cultural Offer',
  D5:  'Service & Third Place',
  D6:  'Digital Maturity & Tech',
  D7:  'Accessibilita Radicale',
  D8:  'Audience & Edutainment',
  D9:  'Governance & Sustainability',
  D10: 'Welfare Culturale & Impatto'
};

