/**
 * Restituisce gli ambiti attivi per un utente (usato da Digest_routing.js).
 * Ritorna array di ambiti [1,2,3,4,5] o [] se nessun filtro.
 */
function getIdentityAmbitiForUser(email) {
  if (!email) return [];
  try {
    var result = getUserIdentity(email);
    if (result.ok && result.identity._hasIdentity) {
      return result.identity._activeAmbiti;
    }
  } catch (_) {}
  return []; // nessun filtro
}

/**
 * Restituisce la regione filtro per un utente (usato da Digest_routing.js).
 * Ritorna stringa regione o '' se tutta Italia.
 */
function getIdentityRegioneForUser(email) {
  if (!email) return '';
  try {
    var result = getUserIdentity(email);
    if (result.ok && result.identity.ambito === 'locale' && result.identity.regione) {
      return result.identity.regione;
    }
  } catch (_) {}
  return '';
}

