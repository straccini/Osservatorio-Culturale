function _identGetUtentiSheet_() {
  try {
    var ss = (typeof getMainSS === 'function') ? getMainSS() : SpreadsheetApp.getActiveSpreadsheet();
    return ss.getSheetByName(OC_UTENTI_SHEET || 'Utenti');
  } catch (_) { return null; }
}

/**
 * Assicura che la colonna IdentityMap esista nel foglio Utenti.
 * Ritorna l'indice (0-based) della colonna.
 */
function _identEnsureColumn_(sh) {
  var headers = sh.getRange(1, 1, 1, sh.getLastColumn()).getValues()[0];
  var idx = headers.indexOf(IDENT_COL_NAME);
  if (idx >= 0) return idx;

  // Aggiungi colonna
  var newCol = sh.getLastColumn() + 1;
  sh.getRange(1, newCol).setValue(IDENT_COL_NAME).setFontWeight('bold');
  Logger.log('[IDENT] Aggiunta colonna ' + IDENT_COL_NAME + ' al foglio Utenti (col ' + newCol + ')');
  return newCol - 1; // 0-based
}

function _identNormalize_(input) {
  var map = JSON.parse(JSON.stringify(IDENT_DEFAULT));

  if (typeof input === 'string') {
    try { input = JSON.parse(input); } catch (_) { return map; }
  }
  if (!input || typeof input !== 'object') return map;

  // Dimensioni
  if (input.dims && typeof input.dims === 'object') {
    for (var d = 1; d <= 10; d++) {
      var key = 'D' + d;
      map.dims[key] = !!(input.dims[key]);
    }
  }

  // Ambito
  if (input.ambito === 'locale') {
    map.ambito = 'locale';
  } else {
    map.ambito = 'nazionale';
  }

  // Regione
  if (input.regione && typeof input.regione === 'string') {
    var reg = input.regione.trim();
    if (IDENT_REGIONI.indexOf(reg) >= 0) {
      map.regione = reg;
    }
  }

  // Notify only matches
  map.notifyOnlyMatches = !!(input.notifyOnlyMatches);

  // Auto-generated flag
  map.autoGenerated = !!(input.autoGenerated);

  // Conteggio per log
  map._activeDimsCount = Object.keys(map.dims).filter(function(d) { return map.dims[d]; }).length;

  return map;
}

function _findCol_(headers, name) {
  for (var i = 0; i < headers.length; i++) {
    if (String(headers[i]).toLowerCase() === name.toLowerCase()) return i;
  }
  return -1;
}

